package com.lazexe.mystatfit.spinnernavigation;

public class SpinnerNavigationItem {

	private String title;
	private int icon;

	public SpinnerNavigationItem(String title, int icon) {
		this.title = title;
		this.icon = icon;
	}

	public String getTitle() {
		return title;
	}

	public int getIcon() {
		return icon;
	}
	
}
